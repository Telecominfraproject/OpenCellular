#include <arch/stages.h>
#include <program_loading.h>

void arch_prog_run(struct prog *prog)
{
	void *cb_tables = prog_entry_arg(prog);
	void (*doit)(void *) = prog_entry(prog);

	doit(cb_tables);
}

int arch_supports_bounce_buffer(void)
{
	return 0;
}
